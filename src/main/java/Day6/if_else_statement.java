package Day6;

public class if_else_statement {
    public static void main(String[] args) {
        int ishaan = 5;
        int krithika = 14;

        if (krithika < ishaan) {
            System.out.println("krithika is younger than ishaan");
        }

        if (ishaan > krithika) {
            System.out.println("ishaan");
        }

        if (krithika < ishaan) {
            System.out.println("krithika");
        } else {
            System.out.println("krithika is older than ishaan");
        }

    }


}