package day1;


public class Hello {

    //instance variables employee

    int empid = 20;
    String empname = "suguna";
    boolean status = false;
    char surname = 'b';


    public static void main(String[] args) {
        Hello obj = new Hello();
        System.out.println("employee id is " + obj.empid);
        System.out.println("employee name is " + obj.empname);
        System.out.println("employee age is " + obj.status);
        System.out.println("employee surname is " + obj.surname);


    }


}































