package day9;

public class Narrow_typecasting {
    //declaring variables

    double var1 = 1000;

    // narrow type casting

    int var2 = (int) var1;

    public static void main(String args[]) {

        //declaring object

        Narrow_typecasting obj = new Narrow_typecasting();

        System.out.println(obj.var2);

    }
}
